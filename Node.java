package tugas2;

public class Node {
	String Label;
	int Value;
	
	void setlabel(String label){
		this.Label = label;
	}
	
	public String getlabel(){
		return this.Label;
	}
	
	void setvalue(int Value){
		this.Value = Value;
	}
	
	public int getValue(){
		return this.Value;
	}
}
